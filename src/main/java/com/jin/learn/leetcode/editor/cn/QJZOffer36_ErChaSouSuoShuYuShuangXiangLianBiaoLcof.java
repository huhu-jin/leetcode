class Solution {

    private Node pre;
    private Node head;

    public Node treeToDoublyList(Node root) {
        if(root == null) return null;
        dfs(root);
        head.left =pre;
        pre.right =head;
        return head;
    }


    private void dfs(Node cur) {
        if(cur == null) return;
        dfs(cur.left);

        if(pre == null){
            head= cur;
            pre = cur;
        }else{
            pre.right = cur;
            cur.left = pre;
            pre = cur;
        }

        dfs(cur.right);
    }

}
